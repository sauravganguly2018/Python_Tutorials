# Operators in python
# Arithmetic operators
# Assignment operators
# Comparison operators
# Logical operators
# Identity operators
# Membership operators
# Bitwise operators

# Arithmetic operators
print("\nArithmetic operators\n")
print("7 + 5 is : ",7+5)
print("7 - 5 is : ",7-5)
print("7 * 5 is : ",7*5)
print("7 / 5 is : ",7/5)
print("7 // 5 is : ",7//5)
print("7 ** 3 is : ",7**3)

# Assignment operators
print("\nAssignment operators\n")
x=7
print(x)
x+=8
print(x)


# Comparison operators
print("\nComparison operators\n")
i=9
print(i>=17)

# Logical operators
print("\nLogical operator\n")
a=True
b=False
print(a and b)
print(a or b)

# Identity operators
print("\nIdentity operator\n")
print(a is b)
print(a is not b)
print(8 is 7)
print(8 is not 7)

# Membership operators
print("\nMembership operator\n")
list1=[1,2,3,6,4]
print(3 in list1)
print(3 not in list1)

# Bitwise operators
print("\nBitwise operator\n")
print(0 & 1)
print(0 | 1)
